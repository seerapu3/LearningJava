package loops;

public class ForEachLoops {
	
	public static void main(String[] args) {
		
		String[] cars = {"Volvo", "BMW", "Ford", "Mazda"};

		for(String str:cars)
		{
			System.out.println(str);
		}
		
		
		int[] i = {10,20,30,40,50};
		for(int j:i)
		{
			System.out.println(j);
		}
		
		
	}

}
